def textParse(bigString):    #input is big string, #output is word list
    #文本切割函数，正则表达式可以去掉标点符号
    import re
    listOfTokens = re.split(r'\W*', bigString)
    return [tok.lower() for tok in listOfTokens if len(tok) > 2] 
    
